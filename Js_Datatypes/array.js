// Array: A collection of values (e.g., ["Hello", "World"])
// Each value of array is seperated by comma(,)

const games = ["Cricket", "Hockey", "Football", "Tennis", "Chess"];
console.log(games); // output is [ 'Cricket', 'Hockey', 'Football', 'Tennis', 'Chess' ]
// Get value by index numbers
// Index of array is started from 0
console.log(games[0]); // output is Cricket
console.log(games[1]); // output is Hockey
console.log(games[2]); // output is Football

