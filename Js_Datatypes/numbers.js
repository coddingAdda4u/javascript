// Number: Represents numeric values (e.g., 42, 3.14
// The number datatype is an integer or a floating point number.

// Integer
const num1 =  20;
const num2 = 10;
console.log(num1, num2); // output 20 10
console.log(typeof(num1, num2)); //output is number 
// float
const float1 = 3.14;
const float2 = 10.3;
console.log(float1, float2);
console.log(typeof(float1, float2));
