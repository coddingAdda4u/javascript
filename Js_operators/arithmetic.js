// It is a types of opertors used for some mathematical operations

// + : Addition
// : :Substraction
// * : Multiplication
// ** : Exponentiational
// / : Division
// % : Modulas
// ++ : Increment
// -- : Decrement

let a = 10;
let b = 20;

// Adition operator
let sum = a + b;
console.log(sum);
// Substraction operator
let sub = a - b;
console.log(sub);
// Multiplication 
let mul = a * b;
console.log(mul);
// Exponentiatial
let exp = a ** b;
console.log(exp);
// Division
let div = a / b;
console.log(div);
// Modulas
let mod = a % b;
console.log(mod);
//Pre Increament (first increament the value then print)
let inc =  ++a;
console.log(inc);
// Post Increament (first print the value then increament) 
inc = a++;
console.log(inc)
//Pre Decreament (first decreament the value then print)
let dec = --b;
console.log(dec);
// Post Decreament (first print the value then decreament) 
inc = b--;
console.log(inc);

