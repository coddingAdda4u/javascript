// Assignment operators used to assign values to variables

// = : Assigns a value.
// += : Adds and assigns.
// = : Subtracts and assigns.
// *= : Multiplies and assigns.
// /= : Divides and assigns.

// Assign opertaor
let a = 10;
let b = 20;
// Add an Assign
console.log(a += b) // output is 30
// Substract and Assign 
console.log(a -= b) // output is 10
// Multipliesand Assign
console.log(a*= b) // output is 200
// Division and Assign
console.log(a /= b) // output is 10
